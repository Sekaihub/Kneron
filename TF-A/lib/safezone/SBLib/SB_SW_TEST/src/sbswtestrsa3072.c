static void SB_SW_RSA_Verify_Image_3072(SB_Result_t expect)
{
    SBIF_SGVector_t vec[1];
    SB_Result_t res;
    uint32_t storage[SBLIB_CFG_STORAGE_SIZE / sizeof(uint32_t)];

    const SBIF_Header_t * const header_p =
        (const SBIF_Header_t *) test_array3072_aligned;

    c_memcpy(test_array3072_aligned, test_array3072_in, sizeof(test_array3072_aligned));

    vec[0].Data_p = (uint32_t *)(((char *) (test_array3072_aligned)) + sizeof(*header_p));
    vec[0].DataLen = sizeof(test_array3072_aligned) - sizeof(*header_p);

    ConvertAreaToDMAResource((void **)discard_const(&header_p), sizeof(*header_p));
    ConvertVectorToDMAResource(vec);
    res = SB_Image_DecryptAndVerify((void *)&storage, NULL,
                                    &test_pk3072, NULL,
                                    header_p, vec, vec, 1);

    FreeDMAResources();
    fail_if(res != expect,
            "Image verification result comparison failure: %d (expected=%d)",
            (int) res, (int) expect);
}

